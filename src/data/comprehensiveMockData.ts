// ==================== Server Health Metrics ====================
const generateHealthMetrics = () => {
  const metrics: any[] = [];
  const now = new Date();

  mockServers.forEach((server) => {
    // Generate last 168 hours (7 days) of metrics
    for (let i = 0; i < 168; i++) {
      const timestamp = new Date(now.getTime() - i * 60 * 60 * 1000);
      
      // Simulate realistic usage patterns
      let cpuUsage = 30 + Math.random() * 40;
      let memoryUsage = 50 + Math.random() * 30;
      let diskUsage = 40 + Math.random() * 30;
      
      // Add some variation based on server type and tier
      if (server.serverType === 'Database') {
        memoryUsage += 10;
        diskUsage += 15;
      }
      
      if (server.tierName === 'large') {
        cpuUsage += 10;
      }
      
      // Occasionally spike to warning/critical levels
      if (Math.random() < 0.05) {
        cpuUsage = 75 + Math.random() * 15;
      }
      if (Math.random() < 0.03) {
        memoryUsage = 85 + Math.random() * 10;
      }
      
      // Server 11 is in maintenance - should show offline
      const isOffline = server.serverId === 11;
      
      let healthStatus: 'Healthy' | 'Warning' | 'Critical' | 'Offline' = 'Healthy';
      if (isOffline) {
        healthStatus = 'Offline';
      } else if (cpuUsage >= 90 || memoryUsage >= 90 || diskUsage >= 95) {
        healthStatus = 'Critical';
      } else if (cpuUsage >= 70 || memoryUsage >= 75 || diskUsage >= 80) {
        healthStatus = 'Warning';
      }
      
      metrics.push({
        metricId: metrics.length + 1,
        serverId: server.serverId,
        serverName: server.serverName,
        serverType: server.serverType,
        cpuUsagePercent: isOffline ? 0 : Math.round(cpuUsage * 10) / 10,
        memoryUsagePercent: isOffline ? 0 : Math.round(memoryUsage * 10) / 10,
        memoryUsedGB: isOffline ? 0 : Math.round(server.ramGB * (memoryUsage / 100) * 10) / 10,
        memoryTotalGB: server.ramGB,
        diskUsagePercent: isOffline ? 0 : Math.round(diskUsage * 10) / 10,
        diskUsedGB: isOffline ? 0 : Math.round(server.storageGB * (diskUsage / 100) * 10) / 10,
        diskTotalGB: server.storageGB,
        networkInMbps: isOffline ? null : Math.round((10 + Math.random() * 90) * 10) / 10,
        networkOutMbps: isOffline ? null : Math.round((5 + Math.random() * 45) * 10) / 10,
        uptimeDays: isOffline ? null : Math.floor(Math.random() * 365),
        activeConnections: isOffline ? null : Math.floor(50 + Math.random() * 200),
        healthStatus,
        responseTimeMs: isOffline ? null : Math.floor(10 + Math.random() * 50),
        isReachable: !isOffline,
        errorMessage: isOffline ? 'Server is in maintenance mode' : null,
        recordedAt: timestamp.toISOString(),
      });
    }
  });

  return metrics;
};

